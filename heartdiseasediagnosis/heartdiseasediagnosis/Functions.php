    <p align="left"  class="a style1">
In mammals, the function of the right side of the heart  is to collect de-oxygenated blood, in the right atrium, from the body (via superior and inferior vena cavae) and pump it, through the tricuspid valve, via the right ventricle, into the lungs (pulmonary circulation) so that carbon dioxide can be dropped off and oxygen picked up (gas exchange). This happens through the passive process of diffusion. The left side  collects oxygenated blood from the lungs into the left atrium. From the left atrium the blood moves to the left ventricle, through the bicuspid valve (mitral valve), which pumps it out to the body (via the aorta). On both sides, the lower ventricles are thicker and stronger than the upper atria. The muscle wall surrounding the left ventricle is thicker than the wall surrounding the right ventricle due to the higher force needed to pump the blood through the systemic circulation.<br>
Starting in the right atrium, the blood flows through the tricuspid valve to the right ventricle. Here, it is pumped out the pulmonary semilunar valve and travels through the pulmonary artery to the lungs. From there, oxygenated blood flows back through the pulmonary vein to the left atrium. It then travels through the mitral valve to the left ventricle, from where it is pumped through the aortic semilunar valve to the aorta. The aorta forks and the blood is divided between major arteries which supply the upper and lower body. The blood travels in the arteries to the smaller arterioles and then, finally, to the tiny capillaries which feed each cell. The (relatively) deoxygenated blood then travels to the venules, which coalesce into veins, then to the inferior and superior venae cavae and finally back to the right atrium where the process began.<br>
The heart is effectively a syncytium, a meshwork of cardiac muscle cells interconnected by contiguous cytoplasmic bridges. This relates to electrical stimulation of one cell spreading to neighboring cells.
Some cardiac cells are self-excitable, contracting without any signal from the nervous system, even if removed from the heart and placed in culture. Each of these cells have their own intrinsic contraction rhythm. A region of the human heart called the sinoatrial (SA) node, or pacemaker, sets the rate and timing at which all cardiac muscle cells contract. The SA node generates electrical impulses, much like those produced by nerve cells. Because cardiac muscle cells are electrically coupled by inter-calated disks between adjacent cells, impulses from the SA node spread rapidly through the walls of the artria, causing both artria to contract in unison. The impulses also pass to another region of specialized cardiac muscle tissue, a relay point called the atrioventricular node, located in the wall between the right atrium and the right ventricle. Here, the impulses are delayed for about 0.1s before spreading to the walls of the ventricle. The delay ensures that the artria empty completely before the ventricles contract. Specialized muscle fibers called Purkinje fibers then conduct the signals to the apex of the heart along and throughout the ventricular walls. The Purkinje fibres form conducting pathways called bundle branches. This entire cycle, a single heart beat, lasts about 0.8 seconds. The impulses generated during the heart cycle produce electrical currents, which are conducted through body fluids to the skin, where they can be detected by electrodes and recorded as an electrocardiogram (ECG or EKG).  The events related to the flow or blood pressure that occurs from the beginning of one heartbeat to the beginning of the next can be referred to a cardiac cycle. 
The SA node is found in all amniotes but not in more primitive vertebrates. In these animals, the muscles of the heart are relatively continuous and the sinus venosus coordinates the beat which passes in a wave through the remaining chambers. Indeed, since the sinus venosus is incorporated into the right atrium in amniotes, it is likely homologous with the SA node. In teleosts, with their vestigial sinus venosus, the main centre of coordination is, instead, in the atrium. The rate of heartbeat varies enormously between different species, ranging from around 20 beats per minute in codfish to around 600 in hummingbirds. 
Cardiac arrest is the sudden cessation of normal heart rhythm which can include a number of pathologies such as tachycardia, an extremely rapid heartbeat which prevents the heart from effectively pumping blood, which is an irregular and ineffective heart rhythm, and asystole, which is the cessation of heart rhythm entirely.
Cardiac tamponade is a condition in which the fibrous sac surrounding the heart fills with excess fluid or blood, suppressing the heart's ability to beat properly. Tamponade is treated by pericardiocentesis, the gentle insertion of the needle of a syringe into the pericardial sac (avoiding the heart itself) on an angle, usually from just below the sternum, and gently withdrawing the tamponading fluids.  </p>  
